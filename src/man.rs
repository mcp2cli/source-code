#[cfg(test)]
mod tests {
    use super::*;
    use crate::config::AppConfig;
    use crate::mcp::model::TransportKind;

    fn http_config(name: &str, endpoint: &str) -> AppConfig {
        let mut cfg = AppConfig::default();
        cfg.server.display_name = format!("{} Server", name);
        cfg.server.transport = TransportKind::StreamableHttp;
        cfg.server.endpoint = Some(endpoint.to_owned());
        cfg
    }

    fn stdio_config(name: &str, cmd: &str) -> AppConfig {
        let mut cfg = AppConfig::default();
        cfg.server.display_name = format!("{} Server", name);
        cfg.server.transport = TransportKind::Stdio;
        cfg.server.stdio.command = Some(cmd.to_owned());
        cfg
    }

    #[test]
    fn generate_http_no_manifest() {
        let config = http_config("work", "http://localhost:3001/mcp");
        let ctx = ManPageContext {
            name: "work",
            config: &config,
            manifest: None,
        };
        let page = generate(&ctx);

        // Title header
        assert!(page.contains(".TH WORK 1"), "missing title header");
        // NAME section
        assert!(page.contains(".SH NAME"), "missing NAME section");
        assert!(page.contains("work \\- "), "missing name line");
        // Transport
        assert!(
            page.contains("Streamable HTTP"),
            "missing transport description"
        );
        assert!(page.contains("localhost:3001"), "missing endpoint in page");
        // Built-in commands
        assert!(page.contains("\\fBls\\fR"), "missing ls");
        assert!(page.contains("\\fBdoctor\\fR"), "missing doctor");
        // No manifest notice
        assert!(
            page.contains("No cached capability inventory"),
            "missing no-manifest notice"
        );
        // SEE ALSO
        assert!(page.contains("mcp2cli (1)"), "missing see also");
    }

    #[test]
    fn generate_stdio_no_manifest() {
        let config = stdio_config("myapp", "./my-server");
        let ctx = ManPageContext {
            name: "myapp",
            config: &config,
            manifest: None,
        };
        let page = generate(&ctx);

        assert!(page.contains(".TH MYAPP 1"));
        assert!(page.contains("Stdio subprocess"));
        assert!(page.contains("./my-server"));
    }

    #[test]
    fn escape_dots_and_backslashes() {
        assert_eq!(esc(".foo"), "\\&.foo");
        assert_eq!(esc("a\\b"), "a\\\\b");
        assert_eq!(esc("normal"), "normal");
        assert_eq!(esc("'quoted"), "\\&'quoted");
    }

    #[test]
    fn install_creates_file() {
        let dir = tempfile::tempdir().unwrap();
        let man_dir = dir.path().join("man1");
        let content = ".TH TEST 1\n.SH NAME\ntest \\- placeholder\n";
        let path = install("test", content, &man_dir).unwrap();
        assert_eq!(path, man_dir.join("test.1"));
        assert_eq!(fs::read_to_string(&path).unwrap(), content);
    }

    #[test]
    fn install_creates_dir_if_missing() {
        let dir = tempfile::tempdir().unwrap();
        let nested = dir.path().join("a/b/c/man1");
        let path = install("cmd", "content", &nested).unwrap();
        assert!(path.exists());
    }
}
