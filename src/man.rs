fn write_manifest_sections(out: &mut String, manifest: &CommandManifest) {
    // Separate commands by kind
    let mut tools: Vec<(&String, &crate::apps::manifest::ManifestCommand)> = Vec::new();
    let mut resources: Vec<(&String, &crate::apps::manifest::ManifestCommand)> = Vec::new();
    let mut prompts: Vec<(&String, &crate::apps::manifest::ManifestCommand)> = Vec::new();
    let mut groups: Vec<(
        &String,
        &str,
        &indexmap::IndexMap<String, crate::apps::manifest::ManifestCommand>,
    )> = Vec::new();

    for (name, entry) in &manifest.commands {
        match entry {
            ManifestEntry::Command(cmd) => match cmd.kind {
                CommandKind::Tool => tools.push((name, cmd)),
                CommandKind::Resource | CommandKind::ResourceTemplate => {
                    resources.push((name, cmd))
                }
                CommandKind::Prompt => prompts.push((name, cmd)),
            },
            ManifestEntry::Group { summary, children } => {
                groups.push((name, summary.as_str(), children));
            }
        }
    }

    if !tools.is_empty() || !groups.is_empty() {
        section(out, "MCP TOOLS");
        ln(
            out,
            "Tools exposed by the MCP server. Each tool becomes a subcommand with typed flags.",
        );
        for (name, cmd) in &tools {
            write_command(out, name, cmd, 0);
        }
        for (gname, gsummary, children) in &groups {
            ln(out, ".TP");
            ln(out, &format!("\\fB{}\\fR", gname));
            ln(out, &format!("{} (command group)", esc(gsummary)));
            for (cname, ccmd) in *children {
                write_command(out, &format!("{} {}", gname, cname), ccmd, 1);
            }
        }
    }

    if !resources.is_empty() {
        section(out, "MCP RESOURCES");
        ln(
            out,
            "Resources exposed by the MCP server. Access with the \\fBget\\fR verb.",
        );
        for (name, cmd) in &resources {
            write_command(out, name, cmd, 0);
        }
    }

    if !prompts.is_empty() {
        section(out, "MCP PROMPTS");
        ln(out, "Prompt templates exposed by the MCP server.");
        for (name, cmd) in &prompts {
            write_command(out, name, cmd, 0);
        }
    }
}

fn write_command(
    out: &mut String,
    name: &str,
    cmd: &crate::apps::manifest::ManifestCommand,
    indent: usize,
) {
    let indent_str = if indent > 0 { ".RS 4\n" } else { "" };
    let dedent_str = if indent > 0 { ".RE\n" } else { "" };

    if !indent_str.is_empty() {
        ln(out, ".RS 4");
    }

    // Build synopsis line with flags
    let mut synopsis = format!("\\fB{}\\fR", name);
    for (flag_name, flag) in &cmd.flags {
        let meta = flag.flag_type.value_name();
        if flag.required {
            if meta.is_empty() {
                synopsis.push_str(&format!(" \\fB--{}\\fR", flag_name));
            } else {
                synopsis.push_str(&format!(" \\fB--{}\\fR \\fI{}\\fR", flag_name, meta));
            }
        } else if meta.is_empty() {
            synopsis.push_str(&format!(" [\\fB--{}\\fR]", flag_name));
        } else {
            synopsis.push_str(&format!(" [\\fB--{}\\fR \\fI{}\\fR]", flag_name, meta));
        }
    }
    if let Some(pos) = &cmd.positional {
        if pos.required {
            synopsis.push_str(&format!(" \\fI{}\\fR", pos.name.to_ascii_uppercase()));
        } else {
            synopsis.push_str(&format!(" [\\fI{}\\fR]", pos.name.to_ascii_uppercase()));
        }
    }
    if cmd.supports_background {
        synopsis.push_str(" [\\fB--background\\fR]");
    }

    ln(out, ".TP");
    ln(out, &synopsis);

    // Description
    if cmd.summary.is_empty() {
        ln(out, "(no description)");
    } else {
        ln(out, &esc(&cmd.summary));
    }

    // Per-flag documentation (only if there are flags with descriptions)
    let flags_with_docs: Vec<_> = cmd
        .flags
        .iter()
        .filter(|(_, f)| f.help.is_some() || f.enum_values.is_some() || f.default.is_some())
        .collect();

    if !flags_with_docs.is_empty() {
        ln(out, ".RS 4");
        for (flag_name, flag) in &flags_with_docs {
            let meta = flag.flag_type.value_name();
            let flag_label = if meta.is_empty() {
                format!("\\fB--{}\\fR", flag_name)
            } else {
                format!("\\fB--{}\\fR \\fI{}\\fR", flag_name, meta)
            };
            ln(out, ".TP");
            ln(out, &flag_label);
            let mut desc = flag
                .help
                .as_deref()
                .map(esc)
                .unwrap_or_else(|| String::new());
            if let Some(ref vals) = flag.enum_values {
                let joined = vals.join(", ");
                if desc.is_empty() {
                    desc = format!("One of: {}.", esc(&joined));
                } else {
                    desc.push_str(&format!("  One of: {}.", esc(&joined)));
                }
            }
            if let Some(ref default) = flag.default {
                desc.push_str(&format!(
                    "  Default: \\fI{}\\fR.",
                    esc(&default.to_string())
                ));
            }
            if flag.required {
                desc.push_str("  \\fB(required)\\fR");
            }
            if desc.is_empty() {
                ln(out, "(no description)");
            } else {
                ln(out, &desc);
            }
        }
        ln(out, ".RE");
    }

    if !dedent_str.is_empty() {
        ln(out, ".RE");
    }
    let _ = indent_str; // suppress unused warning
}

// ---------------------------------------------------------------------------
// Internal helpers — troff emission
// ---------------------------------------------------------------------------

/// Append a single line (with trailing newline) to the output buffer.
fn ln(out: &mut String, text: &str) {
    out.push_str(text);
    out.push('\n');
}

/// Emit a `.SH` section heading.
fn section(out: &mut String, name: &str) {
    ln(out, &format!(".SH {}", name));
}

/// Emit a `.TP` tagged paragraph: label on one line, text on the next.
fn tagged_para(out: &mut String, label: &str, text: &str) {
    ln(out, ".TP");
    ln(out, label);
    ln(out, text);
}

/// Escape text for safe inclusion in troff output.
///
/// Rules:
/// - Leading `.` or `'` on a line would be interpreted as a troff macro — prefix with `\&`.
/// - Backslash `\` must be doubled to `\\`.
/// - Hyphens that are meant as minus signs should use `\-` (renders correctly
///   in terminals and PDFs), but we keep plain `-` for flag names used in the
///   `--flag` synopsis and use `\-` only in paragraph body text.
fn esc(text: &str) -> String {
    let mut result = String::with_capacity(text.len() + 8);
    for line in text.split('\n') {
        if !result.is_empty() {
            result.push('\n');
        }
        // Escape backslashes first
        let escaped = line.replace('\\', "\\\\");
        // Prevent leading . or ' from being treated as macros
        if escaped.starts_with('.') || escaped.starts_with('\'') {
            result.push_str("\\&");
            result.push_str(&escaped);
        } else {
            result.push_str(&escaped);
        }
    }
    result
}

