//! Man page generation for mcp2cli alias applications.
//!
//! When `mcp2cli link create --name <alias>` is called, this module generates
//! a UNIX `man(1)` troff-formatted page and installs it alongside the symlink
//! so that `man <alias>` works immediately.
//!
//! The page is built from three sources:
//! 1. The named [`AppConfig`] — transport, endpoint, display name, profile.
//! 2. The cached [`CommandManifest`] (if available) — all tools, resources and
//!    prompts with their typed flags and descriptions.
//! 3. Hard-coded runtime commands (ls, doctor, inspect, jobs …) that are
//!    always present regardless of server capabilities.
//!
//! # Installation
//!
//! Man pages are written to a `man1/` subdirectory.  The default location
//! mirrors the XDG base-directory convention:
//!
//! ```text
//! ~/.local/share/man/man1/<alias>.1
//! ```
//!
//! Modern Linux `man-db` (Ubuntu/Debian) and macOS `man` both search
//! `~/.local/share/man` without extra configuration.  If the user installs
//! the symlink to a non-standard directory with `--dir`, they can supply
//! `--man-dir` to override the destination.
//!
//! # Format
//!
//! Output is `man(7)` troff/groff macro format — the same format used by
//! bash, git, curl, and virtually every other Unix CLI tool.  No external
//! crate is required; the macros are simple enough to emit directly.

use std::{
    fs,
    path::{Path, PathBuf},
};

use anyhow::{Context, Result};

use crate::{
    apps::manifest::{CommandKind, CommandManifest, ManifestEntry},
    config::AppConfig,
    mcp::model::TransportKind,
};

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/// Everything needed to produce a man page for one alias.
pub struct ManPageContext<'a> {
    /// The alias / symlink name (e.g. `work`).
    pub name: &'a str,
    /// The resolved application config for the alias.
    pub config: &'a AppConfig,
    /// Optional command manifest built from the cached discovery inventory.
    /// When `None`, the MCP commands section is omitted but built-in runtime
    /// commands are still documented.
    pub manifest: Option<&'a CommandManifest>,
}

/// Generate troff-formatted man page content as a `String`.
///
/// The returned string can be written directly to a `.1` file and read by
/// `man`, `nroff`, or `groff`.
pub fn generate(ctx: &ManPageContext<'_>) -> String {
    let mut out = String::new();

    let date = chrono::Utc::now().format("%B %Y").to_string(); // e.g. "March 2026"
    let version = env!("CARGO_PKG_VERSION");
    let name_upper = ctx.name.to_ascii_uppercase();

    // Display name: prefer profile overlay > manifest server_name > config display_name
    let display_name = ctx
        .config
        .profile
        .as_ref()
        .and_then(|p| p.display_name.as_deref())
        .or_else(|| ctx.manifest.and_then(|m| m.server_name.as_deref()))
        .unwrap_or(&ctx.config.server.display_name);

    // ---- Title header -------------------------------------------------------
    ln(
        &mut out,
        &format!(
            ".TH {} 1 \"{}\" \"mcp2cli {}\" \"MCP CLI Applications\"",
            name_upper, date, version
        ),
    );

    // ---- NAME ---------------------------------------------------------------
    section(&mut out, "NAME");
    ln(&mut out, &format!("{} \\- {}", ctx.name, esc(display_name)));

    // ---- SYNOPSIS -----------------------------------------------------------
    section(&mut out, "SYNOPSIS");
    ln(&mut out, ".nf");
    ln(&mut out, &format!(".B {}", ctx.name));
    ln(
        &mut out,
        "[\\fB--json\\fR] [\\fB--output\\fR \\fIFORMAT\\fR] [\\fB--timeout\\fR \\fISECS\\fR]",
    );
    ln(&mut out, "\\fICOMMAND\\fR [\\fICOMMAND\\-OPTIONS\\fR ...]");
    ln(&mut out, ".fi");

    // ---- DESCRIPTION --------------------------------------------------------
    section(&mut out, "DESCRIPTION");
    ln(
        &mut out,
        &format!(
            "\\fB{}\\fR is a dynamic MCP CLI application generated by \\fBmcp2cli\\fR(1), \
bound to the \\fI{}\\fR server.",
            ctx.name,
            esc(display_name)
        ),
    );
    ln(&mut out, ".PP");
    ln(
        &mut out,
        "MCP server tools become subcommands with typed \\fB--flags\\fR derived from JSON \
Schema. Resources are accessible via the \\fBget\\fR verb. Prompts become \
workflow subcommands.",
    );
    ln(&mut out, ".PP");

    // Transport details
    match &ctx.config.server.transport {
        TransportKind::StreamableHttp => {
            let endpoint = ctx.config.server.endpoint.as_deref().unwrap_or("(not set)");
            ln(
                &mut out,
                &format!(
                    "Transport: Streamable HTTP.  Endpoint: \\fI{}\\fR.",
                    esc(endpoint)
                ),
            );
        }
        TransportKind::Stdio => {
            let cmd = ctx
                .config
                .server
                .stdio
                .command
                .as_deref()
                .unwrap_or("(not set)");
            let args_str = ctx.config.server.stdio.args.join(" ");
            if args_str.is_empty() {
                ln(
                    &mut out,
                    &format!(
                        "Transport: Stdio subprocess.  Command: \\fI{}\\fR.",
                        esc(cmd)
                    ),
                );
            } else {
                ln(
                    &mut out,
                    &format!(
                        "Transport: Stdio subprocess.  Command: \\fI{} {}\\fR.",
                        esc(cmd),
                        esc(&args_str)
                    ),
                );
            }
        }
    }

    // ---- GLOBAL OPTIONS -----------------------------------------------------
    section(&mut out, "GLOBAL OPTIONS");
    tagged_para(
        &mut out,
        "\\fB--json\\fR",
        "Emit machine-readable JSON output instead of human-readable text. \
All commands honour this flag.",
    );
    tagged_para(
        &mut out,
        "\\fB--output\\fR \\fIFORMAT\\fR",
        "Output format. One of: \\fIhuman\\fR (default), \\fIjson\\fR, \\fIndjson\\fR.",
    );
    tagged_para(
        &mut out,
        "\\fB--timeout\\fR \\fISECS\\fR",
        "Per-request timeout in seconds. Overrides the value in the config file.",
    );
    tagged_para(
        &mut out,
        "\\fB--background\\fR",
        "Execute the command as a background job and return immediately with a job ID.",
    );
    tagged_para(
        &mut out,
        "\\fB--args-file\\fR \\fIPATH\\fR",
        "Load command arguments from a JSON file. Useful for commands with complex payloads.",
    );

    // ---- BUILT-IN COMMANDS (always present) ---------------------------------
    section(&mut out, "BUILT-IN COMMANDS");
    ln(
        &mut out,
        "These commands are available regardless of server capabilities.",
    );
    tagged_para(
        &mut out,
        "\\fBls\\fR [\\fB--json\\fR]",
        "Discover and list all server capabilities: tools, resources, and prompts. \
The output is cached locally for subsequent calls.",
    );
    tagged_para(
        &mut out,
        "\\fBdoctor\\fR [\\fB--json\\fR]",
        "Run connection and capability diagnostics. Reports transport health, protocol \
version, auth state, and cached capability status.",
    );
    tagged_para(
        &mut out,
        "\\fBinspect\\fR [\\fB--json\\fR]",
        "Show full server capabilities, negotiated protocol information, and metadata.",
    );
    tagged_para(
        &mut out,
        "\\fBget\\fR \\fIURI\\fR [\\fB--json\\fR]",
        "Read a server resource identified by URI.",
    );
    tagged_para(
        &mut out,
        "\\fBjobs list\\fR",
        "List all background jobs and their current status.",
    );
    tagged_para(
        &mut out,
        "\\fBjobs watch\\fR [\\fB--id\\fR \\fIID\\fR | \\fB--latest\\fR]",
        "Stream live status updates for a background job until it completes.",
    );
    tagged_para(
        &mut out,
        "\\fBjobs cancel\\fR [\\fB--id\\fR \\fIID\\fR | \\fB--latest\\fR]",
        "Request cancellation of a running background job.",
    );
    tagged_para(
        &mut out,
        "\\fBauth login\\fR",
        "Start the OAuth login flow for servers that require authentication.",
    );
    tagged_para(
        &mut out,
        "\\fBauth logout\\fR",
        "Clear stored authentication tokens.",
    );
    tagged_para(
        &mut out,
        "\\fBauth status\\fR",
        "Show current authentication state.",
    );

    // ---- MCP TOOLS, RESOURCES, PROMPTS (from cached manifest) --------------
    if let Some(manifest) = ctx.manifest {
        write_manifest_sections(&mut out, manifest);
    } else {
        section(&mut out, "MCP COMMANDS");
        ln(
            &mut out,
            "No cached capability inventory found. Run \\fBls\\fR to discover commands:",
        );
        ln(&mut out, ".PP");
        ln(&mut out, ".nf");
        ln(&mut out, &format!("    {} ls", ctx.name));
        ln(&mut out, ".fi");
        ln(&mut out, ".PP");
        ln(
            &mut out,
            "After discovery, re-run \\fBmcp2cli link create\\fR to regenerate this man page \
with full command documentation.",
        );
    }

    // ---- ENVIRONMENT --------------------------------------------------------
    section(&mut out, "ENVIRONMENT");
    tagged_para(
        &mut out,
        "\\fBMCP2CLI_CONFIG_DIR\\fR",
        "Override the directory where named configs are stored.",
    );
    tagged_para(
        &mut out,
        "\\fBMCP2CLI_DATA_DIR\\fR",
        "Override the directory for runtime state, tokens, and discovery cache.",
    );
    tagged_para(
        &mut out,
        "\\fBMCP2CLI_BIN_DIR\\fR",
        "Override the default symlink directory.",
    );
    tagged_para(
        &mut out,
        "\\fBMCP2CLI_TELEMETRY\\fR",
        "Set to \\fIoff\\fR, \\fI0\\fR, or \\fIfalse\\fR to disable telemetry.",
    );
    tagged_para(
        &mut out,
        "\\fBDO_NOT_TRACK\\fR",
        "Set to \\fI1\\fR to opt out of telemetry (https://consoledonottrack.com/).",
    );
    tagged_para(
        &mut out,
        "\\fBRUST_LOG\\fR",
        "Control log verbosity using tracing filter syntax, e.g. \\fImcp2cli=debug\\fR.",
    );

    // ---- FILES --------------------------------------------------------------
    section(&mut out, "FILES");
    tagged_para(
        &mut out,
        &format!("\\fI~/.local/bin/{}\\fR", ctx.name),
        "Symlink to the mcp2cli binary (default location).",
    );
    tagged_para(
        &mut out,
        &format!("\\fI~/.config/mcp2cli/configs/{}.yaml\\fR", ctx.name),
        "Named configuration file (default location).",
    );
    tagged_para(
        &mut out,
        &format!(
            "\\fI~/.local/share/mcp2cli/instances/{}/state.json\\fR",
            ctx.name
        ),
        "Persisted state: auth sessions, discovery cache, background jobs.",
    );
    tagged_para(
        &mut out,
        &format!("\\fI~/.local/share/man/man1/{}.1\\fR", ctx.name),
        "This man page (default location).",
    );

    // ---- SEE ALSO -----------------------------------------------------------
    section(&mut out, "SEE ALSO");
    ln(&mut out, ".BR mcp2cli (1),");
    ln(&mut out, ".BR man (1),");
    ln(&mut out, ".BR jq (1)");

    // ---- NOTES --------------------------------------------------------------
    section(&mut out, "NOTES");
    ln(
        &mut out,
        "MCP (Model Context Protocol) is an open standard developed by Anthropic that \
defines how AI models connect to tools and data sources. See \
\\fIhttps://modelcontextprotocol.io\\fR for the specification.",
    );
    ln(&mut out, ".PP");
    ln(
        &mut out,
        "\\fBmcp2cli\\fR is built at TSOK \\(em The Source of Knowledge AI Laboratory. \
Source code and documentation: \\fIhttps://github.com/mcp2cli/source-code\\fR.",
    );

    out
}

/// Write the generated page to `<man_dir>/<name>.1`.
///
/// The directory is created if it does not exist. Returns the full path to
/// the installed file.
pub fn install(name: &str, content: &str, man_dir: &Path) -> Result<PathBuf> {
    fs::create_dir_all(man_dir)
        .with_context(|| format!("failed to create man directory: {}", man_dir.display()))?;

    let page_path = man_dir.join(format!("{}.1", name));
    fs::write(&page_path, content)
        .with_context(|| format!("failed to write man page: {}", page_path.display()))?;

    Ok(page_path)
}

